#include <bits/stdc++.h>
using namespace std;

int removeElement(vector<int> &nums, int val)
{
    cin >> nums[];
    cin >> val;

    int count;

    for (int i = 0; i < nums.size(); i++)
    {
        if (nums[i] == val)
        {
            break;
            count++;
        }
    }
    cout << count;
}